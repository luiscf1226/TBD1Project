    public static int numUnique(ArrayList<String> list) {
        if (list == null || list.size() == 0) {
            return 0;
        }
        int counter = 1;

        for (int i = 1; i < list.size(); i++) {
            if (list.get(i - 1) != list.get(i)) {
                ++counter;
            }
        }

        return counter;
    }

